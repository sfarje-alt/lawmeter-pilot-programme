// Get alerts filtered by date range (last N days)
export function getRecentPharmaceuticalAlerts(days: number = 30): PeruAlert[] {
  const cutoffDate = new Date();
  cutoffDate.setDate(cutoffDate.getDate() - days);
  
  return PHARMACEUTICAL_MOCK_ALERTS.filter(alert => {
    const alertDate = new Date(alert.created_at);
    return alertDate >= cutoffDate;
  });
}
